#include <stdio.h>

int main()
{

    // \n = newline - create a newline in the message ex.: Hello\nWorld!
    // \t = tab - create a TAB space between the letters ex.: He\tl\tlo World!
    // \ = can print " or ' in a string ex.: \"Hello\" World!

    printf("Hello\nWorld!\n");
    printf("Hell\to\tWorld!\n");
    printf("\"Hello\" World!");
    return 0;
}